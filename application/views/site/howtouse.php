                    <h3 class="h3howtouse"> How to Create a New Application</h3>

<p>1. Go to DataPlug and click on “Start Using” button to create your application.</p>

<p>2. You can add different data collection widgets from the “Controls” menu.</p>

<p>3. Choose an icon and name of the application and click "Save".</p>

<p>4. Signup to register and verify your e-mail address (if you haven't already).</p>

<p>5. Once email is verified, you can login to "Edit".</p>

<p>6. On edit page click “Build New version” and click to "download" the application.</p>

<br />

<h3 class="h3howtouse">How to Update an Existing DataPlug Application</h3>

<p>1. Login with your existing credentials, make edits, save, and build.</p>

<p>2. Once updated using the control panel, the application installed on the mobile device automatically generates a prompt to sync with the new modified view (on some devices, you may have to "sync" from "application settings" on your phone).</p>

<p>3. The control panel also lets you rollback to a previous version, if needed; DataPlug maintains older versions of your application build.</p>

